#include "libft.h"

void	ft_putnbr(int n)
{
	int	i;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n > 9)
	{
		i = n / 10;
		n -= 10 * i;
		ft_putnbr(i);
	}
	ft_putchar('0' + n);
}
